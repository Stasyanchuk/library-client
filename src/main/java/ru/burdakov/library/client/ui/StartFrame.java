    private void entryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entryButtonActionPerformed
        if(ipTextField.getText().trim().equals("") || portTextField.getText().trim().equals(""))
            errorLabel.setVisible(true);
        else {
            try {
                new MainFrame(ipTextField.getText(), Integer.valueOf(portTextField.getText())).setVisible(true);
                dispose();
            } catch (NullPointerException e){
                e.printStackTrace();
                errorLabel.setVisible(true);
            }
        }
    }//GEN-LAST:event_entryButtonActionPerformed

