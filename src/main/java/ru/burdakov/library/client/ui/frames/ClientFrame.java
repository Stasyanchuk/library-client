    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        client.setLastName(lastNameTextField.getText());
        client.setFirstName(firstNameTextField.getText());
        client.setPassport(Long.valueOf(passportTextField.getText()));
        client.setAge(datePicker.getDate().getTime());
        if(action.equals(Action.ADD))
        clientPanel.addClient(RequestService.addClient(client));
        else {
            RequestService.updateClient(client);
            clientPanel.updateClient(client, clientPanel.getTable().getSelectedRow());
        }
        dispose();
    }//GEN-LAST:event_addButtonActionPerformed

