    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        author.setName(nameTextField.getText());
        author.setDescription(descriptionArea.getText());
        if (action.equals(Action.ADD))
            authorPanel.addAuthor(RequestService.addAuthor(author));
        else {
            RequestService.updateAuthor(author);
            authorPanel.updateAuthor(author, authorPanel.getTable().getSelectedRow());
        }
        dispose();
    }//GEN-LAST:event_addButtonActionPerformed

