    private void initInfo(){
        familyNameLabel.setText(client.getFirstName());
        nameLabel.setText(client.getLastName());
        birthDateLabel.setText(sdf.format(new Date(client.getAge())));
        passportLabel.setText(client.getPassport().toString());
    }

    private void initModel(){
        List<RentEntity> rents = RequestService.getRents(client.getId());
        //FIXME ОБЯЗАТЕЛЬНО ПЕРЕДЕЛАТЬ ЧЕРЕЗ СТРИМЫ В МАПУ
        List<RentEntity> activeRent = rents.stream().filter(r -> r.getStates().equals(RentStates.ACTIVELY)).collect(Collectors.toList());
        List<RentEntity> completedRent = rents.stream().filter(r -> r.getStates().equals(RentStates.COMPLETED)).collect(Collectors.toList());
        this.completedRentTableModel = new CompletedRentTableModel(completedRent);
        activeRentTableModel = new ActiveRentTableModel(activeRent);

        activeTable.setModel(activeRentTableModel);
        activeTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        activeRentPanel.add(activeTable);
        activeRentPanel.add(activeTable.getTableHeader(), BorderLayout.NORTH);

        completedTable.setModel(completedRentTableModel);
        completedTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        cempletedRentPanel.add(completedTable);
        cempletedRentPanel.add(completedTable.getTableHeader(), BorderLayout.NORTH);
    }

